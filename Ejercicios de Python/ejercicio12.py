#Calcular el área y el perímetro de un Rectángulo
alto = int(input("Ingrese el alto del rectangulo: "))
ancho = int(input("Ingrese el ancho del rectangulo: "))
Area = alto * ancho
print("El área del rectangulo es: ", Area, "unidades cuadradas")
Perimetro = 2 *alto + 2 * ancho
print("El perímetro del rectangulo es: ", Perimetro, "unidades")
